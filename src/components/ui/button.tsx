import * as React from "react"
import { Slot } from "@radix-ui/react-slot"
import { cva, type VariantProps } from "class-variance-authority"
import { cn } from "@/lib/utils"

const buttonVariants = cva(
  "inline-flex items-center justify-center whitespace-nowrap rounded-xl text-sm font-medium transition-all duration-200 focus-visible:outline-none focus-visible:ring-2 focus-visible:ring-ring focus-visible:ring-offset-2 disabled:pointer-events-none disabled:opacity-50 disabled:text-[#6B7280]",
  {
    variants: {
      variant: {
        default: "bg-[#F6D365] text-[#22242A] hover:bg-[#F6D365]/90 hover:scale-[1.02] active:scale-[0.98]",
        destructive: "bg-[#F87171] text-white hover:bg-[#F87171]/90 hover:scale-[1.02] active:scale-[0.98]",
        outline: "border border-[#303136] bg-transparent text-[#A1A1AA] hover:bg-[#282A30] hover:text-[#F3F4F6] hover:scale-[1.02] active:scale-[0.98]",
        secondary: "bg-[#282A30] text-[#F3F4F6] hover:bg-[#282A30]/80 hover:scale-[1.02] active:scale-[0.98]",
        ghost: "text-[#A1A1AA] hover:bg-[#282A30] hover:text-[#F3F4F6] hover:scale-[1.02] active:scale-[0.98]",
        link: "text-[#60A5FA] underline-offset-4 hover:underline",
      },
      size: {
        default: "h-10 px-4 py-2",
        sm: "h-9 rounded-lg px-3",
        lg: "h-11 rounded-xl px-8",
        icon: "h-10 w-10",
      },
    },
    defaultVariants: {
      variant: "default",
      size: "default",
    },
  }
)

export interface ButtonProps
  extends React.ButtonHTMLAttributes<HTMLButtonElement>,
    VariantProps<typeof buttonVariants> {
  asChild?: boolean
}

const Button = React.forwardRef<HTMLButtonElement, ButtonProps>(
  ({ className, variant, size, asChild = false, ...props }, ref) => {
    const Comp = asChild ? Slot : "button"
    return (
      <Comp
        className={cn(buttonVariants({ variant, size, className }))}
        ref={ref}
        {...props}
      />
    )
  }
)
Button.displayName = "Button"

export { Button, buttonVariants }
